    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
    Curso cursoSeleccionado = (Curso) cbIdCurso.getSelectedItem();
    Usuario alumnoSeleccionado = (Usuario) cbAlumnoCalificar.getSelectedItem();
    
    try {
        int notaFinal = Integer.parseInt(txtNota.getText());
        
        if (notaFinal > 100) {
            JOptionPane.showMessageDialog(null, "La nota no puede ser mayor a 100", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (cursoSeleccionado != null && alumnoSeleccionado != null) {
            for (Curso curso : ProyectoFinal.listaCurso) {
                if (curso.getIdCurso().equals(cursoSeleccionado.getIdCurso()) && curso.getAlumnoAsignado().contains(alumnoSeleccionado)) {
                    curso.agregarNota(alumnoSeleccionado, notaFinal);
                    JOptionPane.showMessageDialog(this, "Se ha agregado la nota para el alumno: " + alumnoSeleccionado.getNombreUsuario());
                    break;
                }
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese un número válido para la nota", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

