    private void LlenarInfoCurso() {
        Curso cbCarneUSuario = (Curso) cbIdCurso.getSelectedItem();
        for (Curso u : ProyectoFinal.listaCurso) {
            if (u.getIdCurso().equals(cbCarneUSuario.getIdCurso())) {
                txtNombreCurso.setText(u.getNombreCurso());
                txtSeccionCurso.setText(u.getSeccionCurso());
            }
        }
    }

    private void LlenarComboBox() {
        for (Curso u : ProyectoFinal.listaCurso) {
            if(u.getUsuarioAsignado().getUsuarioLogin().equals(usuario.getUsuarioLogin())){
            cbIdCurso.addItem(u);
            }
        }
    }

    private void LlenarComboBoxUsuario() {
        cbAlumnoCalificar.removeAllItems();

        Curso cursoSeleccionado = (Curso) cbIdCurso.getSelectedItem();
        if (cursoSeleccionado != null) {
            for (Usuario alumno : cursoSeleccionado.getAlumnoAsignado()) {
                cbAlumnoCalificar.addItem(alumno);
            }
        }
    }


