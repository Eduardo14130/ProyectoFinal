    private void LlenarInfoCurso() {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Curso idCurso = (Curso) cbIdCurso.getSelectedItem();
            for (Curso u : ProyectoFinal.listaCurso) {
                if (u.getIdCurso().equals(idCurso.getIdCurso())) {
                    txtNombreCurso.setText(u.getNombreCurso());
                    txtSeccionCurso.setText(u.getSeccionCurso());
                    txtFechaInicio.setText(dateFormat.format(u.getFechaInicio()));
                    txtFechaFin.setText(dateFormat.format(u.getFechaFin()));
                    txtHoraInicio.setText(u.getHoraInicio());
                    txtHoraFin.setText(u.getHoraFin());
                    return;
                }
            }
        } catch (ClassFormatError e) {
            System.out.println(e);
        }
    }

    private void LlenarInfoAlumno() {
        for (Usuario u : ProyectoFinal.listaUsuario) {
            if (u.getRolUsuario().equals("Alumno")) {
                cbAlumnoAsignado.addItem(u);
            }
        }
        cbAlumnoAsignado.setSelectedIndex(-1);
    }

    private void LlenarComboBoxCurso() {
        for (Curso u : ProyectoFinal.listaCurso) {
            cbIdCurso.addItem(u);
        }
    }

