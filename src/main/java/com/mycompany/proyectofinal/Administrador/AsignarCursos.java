    private void btnAsignarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarCursoActionPerformed
        Curso idCurso = (Curso) cbIdCurso.getSelectedItem();
        Usuario user = (Usuario) cbAlumnoAsignado.getSelectedItem();
        boolean alumnoAsignado = false;

        if (idCurso.getAlumnoAsignado().contains(user)) {
            JOptionPane.showMessageDialog(this, "El Alumno ya está asignado a este curso.");
            return;
        }

        if (idCurso.getConteoAsignados() >= 10) {
            JOptionPane.showMessageDialog(this, "El curso ya tiene 10 alumnos asignados.");
            return;
        }

        for (Curso curso : ProyectoFinal.listaCurso) {
            if (idCurso.equals(curso) && curso.getConteoAsignados() >= 10) {
                JOptionPane.showMessageDialog(this, "Se ha llegado al limite maximo de 10 alumnos por curso");
                alumnoAsignado = true;
                break;
            }
        }

        int cursosAsignados = 0;
        for (Curso curso : ProyectoFinal.listaCurso) {
            if (curso.getAlumnoAsignado().contains(user)) {
                cursosAsignados++;
            }
        }

        if (cursosAsignados >= 5) {
            JOptionPane.showMessageDialog(this, "El alumno ya está asignado a 5 cursos.");
            return;
        }

        if (!alumnoAsignado) {
            for (Curso curso : ProyectoFinal.listaCurso) {
                if (idCurso.getIdCurso().equals(curso.getIdCurso())) {
                    curso.agregarAlumnos((Usuario) cbAlumnoAsignado.getSelectedItem());
                    JOptionPane.showMessageDialog(this, "Se ha Asignado el Alumno correctamente");
                    break;
                }
            }
        }


    }//GEN-LAST:event_btnAsignarCursoActionPerformed

