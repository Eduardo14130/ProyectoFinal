    private void LlenarcbidCurso() {
        for (Curso curso : ProyectoFinal.listaCurso) {
            cbCurso.addItem(curso);
        }
    }

    private void LlenarlblProfesor() {
        Curso idCurso = (Curso) cbCurso.getSelectedItem();
        for (Curso curso : ProyectoFinal.listaCurso) {
            if (idCurso.equals(curso)) {
                lblProfesor.setText(String.valueOf(idCurso.getUsuarioAsignado()));
            }
        }
    }

    private void ObtenerEstudiantes() {

        Curso cbcurso = (Curso) cbCurso.getSelectedItem();
        DefaultTableModel t = new DefaultTableModel(new String[]{"Carn√©", "Nombre", "Apellido"}, 0);
        jTable1.setModel(t);

        for (Usuario alumno : cbcurso.getAlumnoAsignado()) {
            t.addRow(new Object[]{alumno.getUsuarioLogin(), alumno.getNombreUsuario(), alumno.getApellidoUsuario()});
        }
    }

