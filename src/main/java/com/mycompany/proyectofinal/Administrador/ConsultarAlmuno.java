    private void ObtenerEstudiantes() {

        DefaultTableModel t = new DefaultTableModel(new String[]{"ID", "Carn√©", "Nombre", "Apellido"}, ProyectoFinal.listaUsuario.size());
        jTable1.setModel(t);
        TableModel modelo = jTable1.getModel();
        int fila = 0;

        for (int i = 0; i < ProyectoFinal.listaUsuario.size(); i++) {
            Usuario user = ProyectoFinal.listaUsuario.get(i);
            if (user.getRolUsuario().equals("Alumno")) {
                modelo.setValueAt(user.getUsuarioId(), fila, 0);
                modelo.setValueAt(user.getUsuarioLogin(), fila, 1);
                modelo.setValueAt(user.getNombreUsuario(), fila, 2);
                modelo.setValueAt(user.getApellidoUsuario(), fila, 3);
                fila++;
            }
        }
        ((DefaultTableModel) jTable1.getModel()).setRowCount(fila);
    }


