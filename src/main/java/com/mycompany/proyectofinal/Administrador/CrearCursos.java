    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Usuario cbRolUsuarioAsignado = (Usuario) cbRolUsuario.getSelectedItem();

        try {
            int idCurso = ProyectoFinal.listaCurso.size();
            String nombreCurso = txtNombreCurso.getText();
            String seccionCurso = btnSeccionCurso.getText();
            Date fechaInicio = dateFormat.parse(txtFechaInicio.getText());
            Date fechaFin = dateFormat.parse(txtFechaFin.getText());
            String horaInicio = txtHoraInicio.getText();
            String horaFin = txtHoraFin.getText();

            boolean cursoEncontrado = false;

            for (Curso curso : ProyectoFinal.listaCurso) {
                if (curso.getNombreCurso().equals(txtNombreCurso.getText())) {
                    JOptionPane.showMessageDialog(this, "Curso ya ha sido Ingresado");
                    cursoEncontrado = true;
                }
            }

            if (!cursoEncontrado) {
                Curso curso = new Curso(idCurso, nombreCurso, seccionCurso, fechaInicio, fechaFin, horaInicio, horaFin,cbRolUsuarioAsignado,0);
                ProyectoFinal.listaCurso.add(curso);

                txtNombreCurso.setText("");
                btnSeccionCurso.setText("");
                txtFechaInicio.setText("");
                txtFechaFin.setText("");
                txtHoraInicio.setText("");
                txtHoraFin.setText("");
                
                JOptionPane.showMessageDialog(this, "Curso Creado con Exito");
            }
        } catch (ParseException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

